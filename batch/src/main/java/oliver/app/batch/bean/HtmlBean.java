package oliver.app.batch.bean;

/**
 * Bean for hold HTML
 * 
 * @author lichengwu
 * @version 1.0
 * @created 2013-01-03 4:37 PM
 */
public class HtmlBean {
    /**
     * url
     */
    private String url;
    /**
     * html content
     */
    private String html;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHtml() {
        return html;
    }

    public void setHtml(String html) {
        this.html = html;
    }
}
